# -*- coding: utf-8 -*-
"""
entity_normalizer — нормализация банковских сущностей в тексте.

Заменяет конкретные числа/номера/даты на токены-заглушки, чтобы TF-IDF
не тратил словарный бюджет на вариации одной и той же сущности.

Токены:
  [СУММА]    — денежные суммы (5 000 руб., 3.5 тыс., 10 млн)
  [ТЕЛЕФОН]  — номера телефонов (+7 999 …, 8-800-…)
  [КАРТА]    — номера карт (16 цифр)
  [СЧЁТ]     — номера счётов/договоров (10–20 цифр подряд)
  [ДАТА]     — даты и относительные временны́е выражения
  [БАНК]     — названия российских банков (Сбербанк, Тинькофф и т.д.)
  [ПРОДУКТ]  — типы банковских продуктов (кредитная карта, вклад и т.д.)
"""
from __future__ import annotations

import re
from typing import List, Tuple


# ---------------------------------------------------------------------------
# Паттерны — применяются ПО ПОРЯДКУ (более специфичные — первыми)
# ---------------------------------------------------------------------------

_RULES: List[Tuple[re.Pattern, str]] = [
    # Карты: 16 цифр (с пробелами/тире/звёздочками)
    (re.compile(
        r'\b\d{4}[\s\-*]{0,3}\d{4}[\s\-*]{0,3}'
        r'(?:\d{4}|\*{4})[\s\-*]{0,3}(?:\d{4}|\*{4})\b'
    ), '[КАРТА]'),

    # Телефоны: +7/8 + 10 цифр (разные форматы)
    (re.compile(
        r'(?:\+7|8)[\s\-]?\(?\d{3}\)?[\s\-]?\d{3}[\s\-]?\d{2}[\s\-]?\d{2}'
    ), '[ТЕЛЕФОН]'),

    # Денежные суммы с явным упоминанием валюты/единицы
    (re.compile(
        r'\b\d[\d\s]*'
        r'(?:000\b|тыс(?:яч[а-яё]*)?\b|млн\b|миллион[а-яё]*\b|'
        r'руб(?:л[а-яё]*)?\b|₽|коп(?:е[её]к[а-яё]*)?\b)',
        re.IGNORECASE | re.UNICODE,
    ), '[СУММА]'),

    # Суммы вида 5 000.00 / 1,500 / 10 000
    (re.compile(
        r'\b\d{1,3}(?:[,\s]\d{3})+(?:[.,]\d{1,2})?\b'
    ), '[СУММА]'),

    # Договоры/счета с явным словом-префиксом
    (re.compile(
        r'(?:№|номер|договор[а-яё]*|счёт[а-яё]*|заявк[а-яё]*|'
        r'обращени[а-яё]*)\s*[:\-]?\s*\d{4,}',
        re.IGNORECASE | re.UNICODE,
    ), '[ДОГОВОР]'),

    # Длинные числа (10–20 цифр) — номера счетов, заявок без префикса
    (re.compile(r'\b\d{10,20}\b'), '[СЧЁТ]'),

    # Даты: 23.04.2024 / 2024-04-23 / 23/04/24
    (re.compile(
        r'\b\d{1,2}[.\-/]\d{1,2}[.\-/]\d{2,4}\b'
    ), '[ДАТА]'),

    # Месяцы в родительном падеже (с числом или без)
    (re.compile(
        r'\b(?:\d{1,2}\s+)?'
        r'(?:янв(?:аря)?|фев(?:раля)?|мар(?:та)?|апр(?:еля)?|'
        r'ма(?:я|е)|июн(?:я|е)?|июл(?:я|е)?|авг(?:уста)?|'
        r'сен(?:тября)?|окт(?:ября)?|ноя(?:бря)?|дек(?:абря)?)\b',
        re.IGNORECASE | re.UNICODE,
    ), '[ДАТА]'),

    # Относительные даты
    (re.compile(
        r'\b(?:сегодня|вчера|позавчера|завтра|послезавтра|'
        r'в\s+понедельник|во\s+вторник|в\s+среду|в\s+четверг|'
        r'в\s+пятницу|в\s+субботу|в\s+воскресенье)\b',
        re.IGNORECASE | re.UNICODE,
    ), '[ДАТА]'),

    # Названия российских банков — нормализуем в [БАНК].
    # Список охватывает топ-30 по активам + распространённые варианты написания.
    (re.compile(
        r'\b(?:'
        r'сбербанк[а-яё]*|сбер\b|сберб?[а-яё]*|сбер(?:банк)?|'
        r'тинькофф|тиньков|tinkoff|тинькофф\s+банк|'
        r'альфа[-\s]?банк[а-яё]*|альфабанк[а-яё]*|alfa[-\s]?bank|'
        r'втб\b|втб\s+банк[а-яё]*|'
        r'газпромбанк[а-яё]*|газпром\s+банк[а-яё]*|'
        r'россельхозбанк[а-яё]*|рсхб\b|'
        r'райффайзен[а-яё]*|raiffeisen|'
        r'открытие\s+банк[а-яё]*|банк[а-яё]*\s+открытие|фк\s+открытие|'
        r'совкомбанк[а-яё]*|'
        r'мкб\b|московский\s+кредитный\s+банк[а-яё]*|'
        r'росбанк[а-яё]*|'
        r'промсвязьбанк[а-яё]*|псб\b|'
        r'почта\s+банк[а-яё]*|почтабанк[а-яё]*|'
        r'хоум\s+кредит[а-яё]*|home\s+credit|хоумкредит[а-яё]*|'
        r'ozon\s+банк|озон\s+банк[а-яё]*|'
        r'яндекс(?:\s+банк[а-яё]*|\s+финанс[а-яё]*)?|yandex\s+bank|'
        r'озон\s+банк[а-яё]*|'
        r'ренессанс\s+кредит[а-яё]*|ренессанс\s+банк[а-яё]*|'
        r'ситибанк[а-яё]*|citibank|'
        r'юникредит[а-яё]*|unicredit|'
        r'втб\s*24\b|'
        r'бинбанк[а-яё]*|'
        r'сбп\b|система\s+быстрых\s+платежей'
        r')',
        re.IGNORECASE | re.UNICODE,
    ), '[БАНК]'),

    # Типы банковских продуктов — нормализуем в [ПРОДУКТ].
    (re.compile(
        r'\b(?:'
        r'кредитн(?:ая|ой|ую|ые|ых|ом|им)\s+карт[а-яё]+|'
        r'дебетов(?:ая|ой|ую|ые|ых|ом)\s+карт[а-яё]+|'
        r'карт[а-яё]+\s+(?:visa|mastercard|мир|maestro)|'
        r'(?:visa|mastercard|мир|maestro)\s+карт[а-яё]+|'
        r'потребительск(?:ий|ого|ому|им|ом)\s+кредит[а-яё]*|'
        r'автокредит[а-яё]*|авто\s+кредит[а-яё]*|'
        r'ипотек[а-яё]+|ипотечн(?:ый|ого|ому|ым)\s+кредит[а-яё]*|'
        r'рефинансирован(?:ие|ию|ия|ием)[а-яё]*|'
        r'расчётн(?:ый|ого|ому)\s+счёт[а-яё]*|р[\/\-]с\b|'
        r'накопительн(?:ый|ого)\s+счёт[а-яё]*|'
        r'вклад[а-яё]*|депозит[а-яё]*|'
        r'рассрочк[а-яё]+|беспроцентн[а-яё]+\s+рассрочк[а-яё]+|'
        r'овердрафт[а-яё]*|'
        r'страховк[а-яё]+|страхован(?:ие|ию|ия)|'
        r'инвестицион(?:ный|ного|ному)\s+счёт[а-яё]*|иис\b|'
        r'брокерск(?:ий|ого)\s+счёт[а-яё]*|'
        r'пенсионн(?:ый|ого)\s+счёт[а-яё]*|нпф\b|'
        r'эскроу[а-яё]*|'
        r'аккредитив[а-яё]*'
        r')',
        re.IGNORECASE | re.UNICODE,
    ), '[ПРОДУКТ]'),
]  # конец _RULES

# ---------------------------------------------------------------------------
# Аббревиатуры — раскрываются ДО применения _RULES, чтобы паттерн [СУММА]
# корректно срабатывал на «5 тыс.» и «10 млн.».
# Сортируем от длиннее к короче (longest-match first).
# ---------------------------------------------------------------------------
_ABBREV_MAP = [
    # финансовые единицы
    (re.compile(r'\bтыс(?:яч[а-яё]*)?\b\.?', re.IGNORECASE | re.UNICODE), 'тысяч'),
    (re.compile(r'\bмлрд\b\.?',               re.IGNORECASE | re.UNICODE), 'миллиард'),
    (re.compile(r'\bмлн\b\.?',                re.IGNORECASE | re.UNICODE), 'миллион'),
    (re.compile(r'\bруб\b\.?',                re.IGNORECASE | re.UNICODE), 'рублей'),
    (re.compile(r'\bкоп\b\.?',                re.IGNORECASE | re.UNICODE), 'копеек'),
    # прочие распространённые в банковских текстах
    (re.compile(r'\bкв\b\.?',                 re.IGNORECASE | re.UNICODE), 'квартал'),
    (re.compile(r'\bчел\b\.?',                re.IGNORECASE | re.UNICODE), 'человек'),
    (re.compile(r'\bг\b\.',                   re.IGNORECASE | re.UNICODE), 'год'),
    (re.compile(r'\bмес\b\.?',                re.IGNORECASE | re.UNICODE), 'месяц'),
    (re.compile(r'\bд\b\.',                   re.IGNORECASE | re.UNICODE), 'день'),
]

# Компилируем финальный «очиститель» множественных пробелов
_MULTI_SPACE = re.compile(r'  +')


# ---------------------------------------------------------------------------
# Лимит длины входа (защита от ReDoS — Python re не имеет timeout)
# ---------------------------------------------------------------------------
# Банковские реплики редко длиннее 2 КБ; патологический ввод вида
# "1 1 1 1 …" в паттерне `\b\d[\d\s]*…` приводит к backtracking.
import os as _os


def _max_normalize_input_len() -> int:
    raw = _os.environ.get("MAX_NORMALIZE_INPUT_LEN")
    if not raw:
        return 10_000
    try:
        value = int(raw)
        return value if value > 0 else 10_000
    except ValueError:
        return 10_000


def _expand_abbreviations(text: str) -> str:
    """Раскрывает частые аббревиатуры перед нормализацией сущностей."""
    for pattern, expansion in _ABBREV_MAP:
        text = pattern.sub(expansion, text)
    return text


def normalize_entities(text: str) -> str:
    """Заменяет банковские сущности токенами в одной строке текста.

    Длинные строки обрезаются до ``MAX_NORMALIZE_INPUT_LEN`` (по умолчанию 10 000
    символов) — это защищает от backtracking-DoS в regex-паттернах.
    """
    if not text:
        return text
    limit = _max_normalize_input_len()
    if len(text) > limit:
        text = text[:limit]
    text = _expand_abbreviations(text)
    for pattern, token in _RULES:
        text = pattern.sub(token, text)
    return _MULTI_SPACE.sub(' ', text)
