def hf_cache_key(model_id: str) -> str:
    """Возвращает имя папки кэша HuggingFace для модели model_id.

    HuggingFace хранит модели в {cache_dir}/models--{org}--{name}/.
    Пример: "sentence-transformers/LaBSE" → "models--sentence-transformers--LaBSE"
    """
    return "models--" + model_id.replace("/", "--")


